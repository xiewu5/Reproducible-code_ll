## =========================
## 0. 加载包
## =========================
library(CMplot)

## =========================
## 1. 设置工作目录 & 读取数据
## =========================
# 这里的路径请确保正确
setwd("")

input <- "../indel_cmplot_input"
data  <- read.table(input, header = TRUE)

## =========================
## 2. 【核心提速：P值过滤】
## =========================
# 曼哈顿图底部 80% 的点（P值很大的点）只是背景。
# 只保留 P < 0.2 的点，绘图速度会快 10-20 倍，且图上方信号（显著点）完全不变。
# 如果数据量依然非常大，可以把 0.2 改成 0.1
data_plot <- data[data$P < 0.1, ]

n_snp <- nrow(data_plot) # 记录过滤后的 SNP 数

## =========================
## 3. 颜色设置
## =========================
color_palette <- c(
  "#A6CEE3", "#1F78B4", "#B2DF8A", "#33A02C",
  "#FB9A99", "#FDBF6F", "#FF7F00", "#CAB2D6",
  "#6A3D9A", "#FFFF99", "#B15928"
)

## =========================
## 4. 显著性阈值设置 (按原始总数计算才准确)
## =========================
n_total <- nrow(data)  # 注意：计算阈值要用原始总点数
threshold_use <- c(1, 0.05) / n_total

## =========================
## 5. 画 Manhattan 图 (预览模式)
## =========================
## =========================
## 5. 画 Manhattan 图
## =========================

# 1. 强行设置画布参数 (放在 CMplot 之前)
# mar = c(下, 左, 上, 右)。
# 我们把左边距(第二个值)调小到 2.5 或 3（原来默认可能是 5 或 6）
# mgp = c(标题, 刻度数字, 轴线)。第二个值 0.5 会让数字紧贴轴线。
## =========================
## 5. 画 Manhattan 图
## =========================

# 先运行这一行，设置全局绘图参数
# mgp 的三个数字分别代表：[1]坐标轴标题距离, [2]坐标轴刻度数字距离, [3]坐标轴线距离
# 我们把 [2] 和 [3] 都调小，甚至把 [3] 设为负数（只有 mgp 的第3个值有时可以微调为负数来让轴线贴近数据）
par(mgp = c(1.5, 0.2, -0.5), mar = c(3, 3, 2, 1))

CMplot(
  data_plot,
  plot.type = "m",
  LOG10      = TRUE,
  file.output = FALSE, 
  
  # 删掉所有报错的参数 (cex.axis, tck, mar等都不写在里面)
  
  cex = 0.4,
  col = color_palette,
  threshold      = threshold_use,
  threshold.col = c("grey", "black"),
  threshold.lty = c(1, 2),
  threshold.lwd = c(1, 1),
  amplify    = TRUE,
  signal.cex = c(1, 1),
  signal.pch = c(20, 20),
  signal.col = c("red", "blue")
)
